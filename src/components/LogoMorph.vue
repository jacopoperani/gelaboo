<script setup>
import { ref, onMounted, onUnmounted } from 'vue'
import { gsap } from 'gsap'
// ScrollTrigger è registrato globalmente in main.js. Importato qui solo
// come riferimento per la logica scroll-linked che aggiungeremo allo step
// successivo — nessuna logica di scroll in questo step.
import { ScrollTrigger } from 'gsap/ScrollTrigger'

const logoRef = ref(null)
let bounceTween = null
let floatTweens = []

// Moltiplicatore d'ampiezza del floating (0..1), letto live dai modifiers
// di ogni tween. App.vue lo pilota dal progress dello ScrollTrigger:
// 1 = logo grande in Hero (floating pieno), 0 = logo docked in header (fermo).
const floatAmp = { value: 1 }

function setFloatAmplitude(a) {
  floatAmp.value = Math.max(0, Math.min(1, a))
}

// Bounding box delle 7 lettere in unità viewBox (667.85 × 280.28). getBBox
// ignora i transform CSS/GSAP (scale, floating, fixed): geometria statica,
// stabile a prescindere da scale/scroll. Usato da GelatiFloating per i box
// di collisione per-lettera.
function getLetterBBoxes() {
  const groups = logoRef.value?.querySelectorAll('g[id^="lettera_"]') ?? []
  return Array.from(groups).map((g) => {
    const b = g.getBBox()
    return { id: g.id, x: b.x, y: b.y, width: b.width, height: b.height }
  })
}
// Offset y corrente di ogni lettera (unità viewBox), letto live dal tween di
// floating. Il valore di gsap.getProperty è già il renderizzato post-modifier
// → include l'attenuazione floatAmp (vedi setFloatAmplitude). Ordine DOM
// stabile, accoppiabile per indice con getLetterBBoxes().
function getLetterOffsets() {
  const groups = logoRef.value?.querySelectorAll('g[id^="lettera_"]') ?? []
  return Array.from(groups).map((g) => ({
    id: g.id,
    y: parseFloat(gsap.getProperty(g, 'y')) || 0,
  }))
}
defineExpose({ setFloatAmplitude, getLetterBBoxes, getLetterOffsets })

// Floating continuo per-lettera: oscillazione verticale leggera, infinita,
// con delay scaglionato (non sincrono). Il modifier scala la y corrente per
// floatAmp, così l'ampiezza si spegne dolcemente quando App.vue la porta a 0.
function startFloating(letterGroups) {
  floatTweens = letterGroups.map((el, i) =>
    gsap.to(el, {
      y: -15,
      duration: 1.7,
      ease: 'sine.inOut',
      repeat: -1,
      yoyo: true,
      delay: i * 0.12,
      modifiers: {
        y: (val) => parseFloat(val) * floatAmp.value,
      },
    })
  )
}

onMounted(() => {
  const prefersReducedMotion = window.matchMedia(
    '(prefers-reduced-motion: reduce)'
  ).matches

  const letterGroups = Array.from(
    logoRef.value.querySelectorAll('g[id^="lettera_"]')
  )

  if (prefersReducedMotion) {
    // Niente bounce né floating: lettere statiche e visibili.
    gsap.set(logoRef.value, { opacity: 1 })
    gsap.set(letterGroups, { opacity: 1, y: 0 })
    return
  }

  gsap.set(logoRef.value, { opacity: 1 })
  gsap.set(letterGroups, { opacity: 0, y: 20 })

  bounceTween = gsap.to(letterGroups, {
    y: 0,
    opacity: 1,
    duration: 0.9,
    ease: 'bounce.out',
    stagger: 0.05,
    onComplete: () => startFloating(letterGroups),
  })
})

onUnmounted(() => {
  bounceTween?.kill()
  floatTweens.forEach((t) => t.kill())
})
</script>

